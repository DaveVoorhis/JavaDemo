public class HelloWorld {

	// Note that 'main' is lower case. In Java, method names should start with a lower-case letter.
	// Note that per Java style convention, the initial curly brace goes at the end of the line.
	public static void main(String[] args) {
		// This is the equivalent to C#'s Console.WriteLine
		System.out.println("Jello, Whirled");
	}

}
